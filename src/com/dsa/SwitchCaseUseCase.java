package com.dsa;

public class SwitchCaseUseCase {

	public static void main(String[] args) {

		char alphabet = 'u';
		switch (alphabet) {
		case 'a':
			System.out.println("this is a vowel");
			break;
		case 'e':
			System.out.println("this is a vowel");
			break;
		case 'i':
			System.out.println("this is a vowel");
			break;
		case 'o':
			System.out.println("this is a vowel");
			break;
		case 'u':
			System.out.println("this is a vowel");
			break;

		default:
			System.out.println("this is consonant");

			break;
		}

	}

}
